# Desafio: Sorteio de equipes para um jogo de sala de aula
# Descrição: Você é responsável por organizar uma competição entre os alunos da sua turma. O professor decidiu dividir a turma em equipes aleatórias. Para isso, você deve criar um programa que:
#
# Leia os nomes dos alunos.
# Divida esses alunos em equipes aleatórias de um tamanho específico (o número de alunos por equipe será fornecido pelo usuário).
# Exiba o nome das equipes e os alunos sorteados para cada equipe.
# Requisitos:
#
# O programa deve ler o nome de N alunos (o número de alunos pode ser qualquer valor).
# O programa deve pedir para o usuário informar o número de alunos por equipe.
# O programa deve embaralhar os nomes dos alunos e dividir os alunos em equipes aleatórias.
# O programa deve exibir as equipes com os nomes dos alunos sorteados para cada equipe.
# Se não for possível dividir os alunos de maneira exata (por exemplo, 7 alunos em 3 equipes de 2), o programa deve distribuir os alunos de maneira que as equipes fiquem o mais equilibradas possível.